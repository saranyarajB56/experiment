# lambda function

#   print fullname
fullname = lambda x,y,z:x+y+z
print(fullname(" mariam" , "sara" , "johny"))

# squre root of a number
sqr = lambda a: a**2
print(sqr(10))

# Average of 5 numbers
avg = lambda a,b,c,d,e:(a+b+c+d+e)/5
print(avg(10,20,30,40,50))

# perimeter of a circle
peri = lambda r:(2*3.14*r)
print(peri(10))